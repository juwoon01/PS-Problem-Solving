#include <iostream>
using namespace::std;

void star(int i, int j, int n){
  if(i%3 == 1 && j%3 == 1)
    cout << ' ';
  else if( (i%n > (n/3)-1 && i%n < 2*(n/3)) && (j%n > (n/3)-1 && j%n < 2*(n/3)) )
    cout << ' ';
  else{
    if(n == 3)
      cout << '*';
    else
      star(i,j,n/3);
  }

}

int main(void){
  int n;
  cin >> n;

  for(int i=0;i<n;i++){
    for(int j=0;j<n;j++){
      star(i,j,n);

    }
    cout << endl;
  }



  return 0;
}
